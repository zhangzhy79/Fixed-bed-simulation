%%计算平衡吸附量，双位点朗格缪尔模型
function qe=DSL(p)
%% 参数设置
b1=[2.44 0.19 0.09];    % bar^-1
b2=[88.3 0 0];          % bar^-1
qm1=[1.97 3.39 3.47];   % mol*kg^-1
qm2=[2.68 0 0];         % mol*kg^-1
M=[44 16 28];           % g/mol
%% 计算公式
qe=qm1.*M.*b1.*p./(1+sum(b1.*p))+qm2.*M.*b2.*p./(1+sum(b2.*p)); % g/kg = mg/g